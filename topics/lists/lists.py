#emptylist
print([])

#1d list - homogenous list 
print([1,2,3,4,5])

# 2d list - nested list 
print([1,2,3,[4,5,6],[7,8,9]])

# 3d list - nested list
print([[[1,2,3]],[[1,5,3],[4,5,6],[7,8,9]]])

#heterogenous list
print([1, "hello", 3.14, True])

# using type conversion
print(list("hello world")) # converts string to list of characters